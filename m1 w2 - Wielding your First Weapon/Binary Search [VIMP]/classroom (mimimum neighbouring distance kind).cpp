#include <bits/stdc++.h>
using namespace std;

#define endl "\n"
#define lli long long

lli check(lli a[], lli n, lli x, lli k){
// if minimum neighbouring distance is >= x for k students is possible
    lli i, stud=1, prev=a[0];
    for(i=1; i<n; i++){
        if((a[i]-prev) >= x){
            stud++;
            prev = a[i];
        }
    }
    if(stud >= k)
        return 1;
    else{
        return 0;
    }
}

void solve()
{
    lli n, k, i, lo=1001001001, hi, mid, ans;
    cin>>n>>k;
    lli a[n];
    for(i=0; i<n; i++){
        cin>>a[i];
    }
    sort(a,a+n);
    for(i=1; i<n; i++){
        lo = min(lo,a[i]-a[i-1]);
    }
    hi = a[n-1]-a[0];
// for(i=0; i<n; i++){
//     cout<<a[i]<<" ";
// }
// cout<<endl<<hi<<" "<<lo<<endl;
    while(lo<=hi){
        mid = (lo+hi)/2;
        if(check(a,n,mid,k) == 1){
// if minimum neighbouring distance is >= mid for k students is possible
            ans = mid;
            lo = mid + 1;
        }
        else{
            hi = mid - 1;
        }
    }
    cout<<ans<<endl;
}

signed main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);cout.tie(0);

    int _t; cin>>_t; while(_t--)
    solve();
}


